public class Calculator {

    // membuat function untuk penjumlahan dengan return berupa tipe data int
    // memerlukan 2 parameter berupa int yang nantinya akan dijumlahkan
    public int add(int a, int b) {
        return a + b;
    }

    // membuat function pengurangan dengan return berupa tipe data int
    // memerlukan 2 parameter berupa int yang nantinya akan dikurangi
    public int substract(int a, int b) {
        return a - b;
    }
}
